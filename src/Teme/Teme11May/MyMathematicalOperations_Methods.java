package Teme.Teme11May;

public class MyMathematicalOperations_Methods {


    static int adunare(int x, int y){
        return x + y;
    }

    static int impartire(int x,int  y){
        return x / y;
    }

    static int inmultire(int x, int y){
        return x * y;
    }

    static int restulImpartirii(int x, int y){
        return x % y;
    }

    static int scadere(int x, int y){
        return x - y;
    }
}
